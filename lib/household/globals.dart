class Global {
  static var _hhid = "";
  static var _uid = "";
  static var _slid = "";
  static var _siid = "";

  static String gethhid() {
    return _hhid;
  }

  static void sethhid(id) {
    _hhid = id;
  }

  static String getuid() {
    return _uid;
  }

  static void setuid(id) {
    _uid = id;
  }

  static String getslid() {
    return _slid;
  }

  static void setslid(id) {
    _slid = id;
  }

  static String getsiid() {
    return _siid;
  }

  static void setsiid(id) {
    _siid = id;
  }
}
